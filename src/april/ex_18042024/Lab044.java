package april.ex_18042024;

public class Lab044 {
    public static void main(String[] args) {

        // Increment (++) / Decrement (--) Operators

        // pre,post

        // pre --> Value is incremented first and then the result is completed.

        int a = 10;
        // ++a --> a = a+1 (Value increase first --> assign the value)


        int b = ++a; // a = a+1
        System.out.println(b);

    }

}
